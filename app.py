def cached_predict(text: str, model_choice: str) -> Optional[Dict[str, Any]]:
    """Wrapper around `unified_predict` with simple caching via session_state.

    We avoid heavy st.cache_data here because the underlying models may be
    large and already cached by their own loaders.
    """
    key = f"pred:{model_choice}:{hash(text)}"
    if key in st.session_state:
        return st.session_state[key]
    try:
        res = unified_predict(text, model_choice=model_choice)
        st.session_state[key] = res
        return res
    except Exception as e:
        st.error(f"Prediction failed: {e}")
        logger.exception("Prediction failed for text: %s", text)
        return None


def _normalize_probs_for_display(prob_values: Any) -> list[float]:
    if prob_values is None:
        return [0.0] * len(LABELS)

    arr = np.asarray(prob_values, dtype=float).reshape(-1)
    if arr.size == 0:
        return [0.0] * len(LABELS)
    if arr.size < len(LABELS):
        padded = np.zeros(len(LABELS), dtype=float)
        padded[: arr.size] = arr
        arr = padded
    elif arr.size > len(LABELS):
        arr = arr[: len(LABELS)]

    arr = np.clip(arr, 0.0, None)
    arr = np.nan_to_num(arr, nan=0.0, posinf=0.0, neginf=0.0)
    total = float(arr.sum())
    if not np.isfinite(total) or total <= 0:
        return [1.0 / len(LABELS)] * len(LABELS)
    return (arr / total).tolist()


