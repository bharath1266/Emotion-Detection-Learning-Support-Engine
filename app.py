def _load_evaluation_comparison():
    """Load model comparison artifacts generated by training/evaluate_models.py."""
    import json as _json
    from pathlib import Path

    results_dir = Path(__file__).resolve().parent / "results"
    candidate_csvs = [results_dir / "compare_models.csv", results_dir / "model_comparison.csv"]
    candidate_jsons = [results_dir / "compare_models.json", results_dir / "model_comparison.json"]

    out = {"compare_df": None, "compare_json": None, "artifacts_present": False}
    for csv_path in candidate_csvs:
        if csv_path.exists():
            try:
                out["compare_df"] = pd.read_csv(csv_path)
                break
            except Exception:
                out["compare_df"] = None

    for json_path in candidate_jsons:
        if json_path.exists():
            try:
                out["compare_json"] = _json.loads(json_path.read_text(encoding="utf-8"))
                break
            except Exception:
                out["compare_json"] = None

    if out["compare_df"] is not None:
        expected_cols = {"model", "accuracy", "precision", "recall", "f1"}
        missing = expected_cols.difference(set(out["compare_df"].columns))
        if missing:
            out["compare_df"] = None

    out["artifacts_present"] = (out["compare_df"] is not None) or (out["compare_json"] is not None)
    out["results_dir"] = str(results_dir)
    out["cm_bilstm_path"] = str(results_dir / "confusion_matrix_bilstm.png")
    out["cm_bert_path"] = str(results_dir / "confusion_matrix_bert.png")
    return out


